#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (fd == -1)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		num = '0' + n % 10;
		write(fd, &num, 1);
	}
	if (n < 10)
	{
		num = '0' + n;
		write(fd, &num, 1);
	}
}
