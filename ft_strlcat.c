#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	dlen;
	size_t	slen;
	size_t	i;

	if (!dest && !size)
		return (ft_strlen(src));
	dlen = ft_strlen(dest);
	slen = ft_strlen((char *)src);
	i = 0;
	if (size <= dlen)
		return (slen + size);
	while (src[i] && dlen + i < size - 1)
	{
		dest[dlen + i] = src[i];
		i++;
	}
	dest[dlen + i] = '\0';
	return (dlen + slen);
}
